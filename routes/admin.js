require("dotenv").config();
const express = require("express");
const router  = express.Router();
const jwt     = require("jsonwebtoken");
const User    = require("../models/User");
const Student = require("../models/Student");
const Internship = require("../models/Internship");

// Auth middleware — admin only
const adminMiddleware = (req, res, next) => {
  try {
    const token = req.headers.authorization?.split(" ")[1];
    if (!token) return res.status(401).json({ message: "No token" });
    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    if (decoded.role !== "admin") {
      return res.status(403).json({ message: "Admin access only!" });
    }
    req.user = decoded;
    next();
  } catch (err) {
    res.status(401).json({ message: "Invalid token" });
  }
};

// Get all stats
router.get("/stats", adminMiddleware, async (req, res) => {
  try {
    const allUsers       = await User.find();
    const students       = allUsers.filter(u => u.role === "student");
    const companies      = allUsers.filter(u => u.role === "company");
    const internships    = await Internship.find();
    const studentProfiles = await Student.find();

    // Combine student user + profile data
    const studentData = students.map(function(user) {
      const profile = studentProfiles.find(function(p) {
        return p.userId.toString() === user._id.toString();
      });
      return {
        _id:     user._id,
        name:    user.name,
        email:   user.email,
        college: profile ? profile.college : "",
        degree:  profile ? profile.degree  : "",
        skills:  profile ? profile.skills  : []
      };
    });

    res.json({
      stats: {
        totalStudents:     students.length,
        totalCompanies:    companies.length,
        totalInternships:  internships.length,
        totalApplications: 0
      },
      students:     studentData,
      companies:    companies,
      internships:  internships
    });



































































































































































































































































































































































































































































































































































































































































































































































































    

  } catch (err) {
    console.log("Admin stats error:", err.message);
    res.status(500).json({ message: err.message });
  }
});

// Delete internship
router.delete("/internship/:id", adminMiddleware, async (req, res) => {
  try {
    await Internship.findByIdAndDelete(req.params.id);
    res.json({ message: "Internship deleted!" });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
});

// Delete user
router.delete("/user/:id", adminMiddleware, async (req, res) => {
  try {
    await User.findByIdAndDelete(req.params.id);
    await Student.findOneAndDelete({ userId: req.params.id });
    res.json({ message: "User deleted!" });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
});

module.exports = router;
